#!/usr/bin/env python3
"""Build a local Chloe identity LoRA dataset from the approved manifest."""

from __future__ import annotations

import json
import shutil
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parents[1]
MANIFEST_PATH = REPO_ROOT / "studio/training/chloe_lora_v1/manifest.json"


def main() -> None:
    manifest = json.loads(MANIFEST_PATH.read_text(encoding="utf-8"))
    output_root = REPO_ROOT / manifest["local_output_dir"]
    image_dir = output_root / "images"
    caption_dir = output_root / "captions"
    image_dir.mkdir(parents=True, exist_ok=True)
    caption_dir.mkdir(parents=True, exist_ok=True)

    copied = []
    for index, item in enumerate(manifest["approved_reference_sources"], start=1):
        source = REPO_ROOT / item["path"]
        if not source.exists():
            raise FileNotFoundError(f"Missing source image: {source}")

        stem = f"{index:03d}_{source.stem}"
        target_image = image_dir / f"{stem}{source.suffix.lower()}"
        target_caption = caption_dir / f"{stem}.txt"
        sidecar_caption = image_dir / f"{stem}.txt"

        shutil.copy2(source, target_image)
        target_caption.write_text(item["caption"] + "\n", encoding="utf-8")
        sidecar_caption.write_text(item["caption"] + "\n", encoding="utf-8")
        copied.append((target_image, target_caption))

    readme = output_root / "README.md"
    readme.write_text(
        "\n".join(
            [
                "# Chloe LoRA v1 Local Dataset",
                "",
                "Generated by `scripts/prepare_chloe_lora_identity_dataset.py`.",
                "",
                f"Trigger token: `{manifest['trigger_token']}`",
                f"Image count: {len(copied)}",
                "",
                "This local folder is ignored by git. The source manifest and rules",
                "live in `studio/training/chloe_lora_v1/`.",
                "",
            ]
        ),
        encoding="utf-8",
    )

    print(f"Wrote {len(copied)} Chloe identity references to {output_root}")


if __name__ == "__main__":
    main()
